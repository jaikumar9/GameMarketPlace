function Swap() {
  const [buyValue, setBuyValue] = useState("");
  const [withdrawValue, setWithdrawValue] = useState("");
  const [buyErrorMessage, setBuyErrorMessage] = useState("");
  const [withdrawErrorMessage, setWithdrawErrorMessage] = useState("");
  const [isDepositing, setIsDepositing] = useState(false);

  const handleBuyChange = (e) => {
    const { value } = e.target;
    if (/^\d*$/.test(value)) {
      setBuyValue(value);
      setBuyErrorMessage(value !== "" && Number(value) < 2 ? "Minimum Buy amount is 2" : "");
    } else {
      setBuyErrorMessage("Please type only numbers");
    }
  };

  const handleWithdrawChange = (e) => {
    const { value } = e.target;
    if (/^\d*$/.test(value)) {
      setWithdrawValue(value);
      setWithdrawErrorMessage(value !== "" && Number(value) < 10000 ? "Minimum Withdraw amount is 10000 GT" : "");
    } else {
      setWithdrawErrorMessage("Please type only numbers");
    }
  };

  const handleDeposit = async () => {
    if (!buyValue || Number(buyValue) < 2) return;

    try {
      setIsDepositing(true);
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const signer = provider.getSigner();

      const usdtContract = new ethers.Contract(USDT_CONTRACT_ADDRESS, USDT_ABI, signer);
      const depositContract = new ethers.Contract(DEPOSIT_CONTRACT_ADDRESS, DEPOSIT_ABI, signer);

      const amount = parseUnits(buyValue, 18);

      const approveTx = await usdtContract.approve(DEPOSIT_CONTRACT_ADDRESS, amount);
      await approveTx.wait();

      const depositTx = await depositContract.buyTokensWithUSDT(amount);
      await depositTx.wait();

      setBuyValue("");
      alert("Deposit successful!");
    } catch (error) {
      console.error("Deposit error:", error);
      alert("Error during deposit. Please check console for details.");
    } finally {
      setIsDepositing(false);
    }
  };

  const handleWithdraw = async () => {
    if (!withdrawValue || Number(withdrawValue) < 2000) return;

    try {
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const signer = provider.getSigner();

      const depositContract = new ethers.Contract(DEPOSIT_CONTRACT_ADDRESS, DEPOSIT_ABI, signer);
      const gtContract = new ethers.Contract(GT_CONTRACT_ADDRESS, GT_ABI, signer);

      const amount = parseUnits(withdrawValue, 18);

      const approveTx = await gtContract.approve(DEPOSIT_CONTRACT_ADDRESS, amount);
      await approveTx.wait();

      const withdrawTx = await depositContract.WithdrawUSDT_with_GT(amount);
      await withdrawTx.wait();

      setWithdrawValue("");
      alert("Withdrawal successful!");
    } catch (error) {
      console.error("Withdrawal error:", error);
      alert("Error during withdrawal. Please check console for details.");
    }
  };

  return (
    <>
      <div className="backsideImage">
        <div className="container">
          <Navbar />
          <div className="row mt-5">
            <div className="d-flex justify-content-center align-items-center mt-5">
              <div
                className="w-100 h-auto p-5 d-flex flex-column border-0 rounded-3"
                style={{
                  background: "radial-gradient(30.7% 30.7% at 50% 50%, #382C36 0%, #292128 100%)",
                }}
              >
                <div className="d-flex justify-content-between">
                  <p className="text-white fw-bold mb-0">Buy with USDT/USDC</p>
                  <div className="gt-btn">
                    <button className="btn btn-outline-light d-flex align-items-center">
                      <CiWallet size={20} className="me-2" /> GT:-20000
                    </button>
                  </div>
                </div>

                <div className="row justify-content-between p-3 gap-3">
                  <div className="col-md-5 buy-div border rounded-3 border-0 p-4" style={{ backgroundColor: "#3A3138" }}>
                    <label htmlFor="buyInput" className="form-label text-white">Buy</label>
                    <input
                      type="text"
                      id="buyInput"
                      value={buyValue}
                      onChange={handleBuyChange}
                      placeholder="Type only numbers..."
                      className="form-control border-0"
                      style={{ backgroundColor: "black", color: "white", padding: "10px", borderRadius: "4px" }}
                    />
                    {buyErrorMessage && <div style={{ color: "red", marginTop: "5px" }}>{buyErrorMessage}</div>}
                    <div className="text-white d-flex justify-content-end p-1">
                      <CiDollar size={25} />
                      <span className="ms-1">Minimum:2</span>
                    </div>
                    <button
                      className="text-white rounded-pill"
                      onClick={handleDeposit}
                      disabled={isDepositing || !buyValue || Number(buyValue) < 2}
                    >
                      {isDepositing ? "Depositing..." : "Deposit"}
                    </button>
                  </div>

                  <div className="col-md-5 buy-div border rounded-3 border-0 p-4" style={{ backgroundColor: "#3A3138" }}>
                    <label htmlFor="withdrawInput" className="form-label text-white">Withdraw</label>
                    <input
                      type="text"
                      id="withdrawInput"
                      value={withdrawValue}
                      onChange={handleWithdrawChange}
                      placeholder="Type only numbers..."
                      className="form-control border-0"
                      style={{ backgroundColor: "black", color: "white", padding: "10px", borderRadius: "4px" }}
                    />
                    {withdrawErrorMessage && <div style={{ color: "red", marginTop: "5px" }}>{withdrawErrorMessage}</div>}
                    <div className="text-white d-flex justify-content-end p-1">
                      <CiDollar size={25} />
                      <span className="ms-1">Minimum:10,000 GT</span>
                    </div>
                    <button
                      className="text-white rounded-pill"
                      onClick={handleWithdraw}
                      disabled={!withdrawValue || Number(withdrawValue) < 2000}
                    >
                      Withdraw
                    </button>
                  </div>
                </div>
              </div>
            </div>
          </div>
        </div>
        <Footer />
      </div>
    </>
  );
}

export default Swap;
